#include "libft.h"
#include <stdlib.h>

void	*ft_calloc(unsigned int count, unsigned int size)
{
	void			*ptr;
	unsigned int	i;

	ptr = malloc(count * size);
	if (ptr == NULL)
		return (NULL);
	i = 0;
	while (i < count * size)
	{
		((char *)ptr)[i] = 0;
		i++;
	}
	return (ptr);
}
